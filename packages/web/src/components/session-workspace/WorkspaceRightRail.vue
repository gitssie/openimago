<template>
  <aside class="right-rail" aria-label="工作区右栏">
    <!-- ── Tab strip ─────────────────────────────────────────────── -->
    <div
      ref="tablistEl"
      class="right-rail__tabs"
      role="tablist"
      aria-label="右栏视图"
      @keydown="onTablistKeydown"
    >
      <button
        v-for="tab in TABS"
        :id="`right-rail-tab-${tab.key}`"
        :key="tab.key"
        type="button"
        class="right-rail__tab"
        :class="{ 'right-rail__tab--active': activeTab === tab.key }"
        role="tab"
        :aria-selected="activeTab === tab.key"
        :aria-controls="`right-rail-panel-${tab.key}`"
        :tabindex="activeTab === tab.key ? 0 : -1"
        @click="selectTab(tab.key)"
      >
        <span class="right-rail__tab-label">{{ tab.label }}</span>
        <span
          v-if="tab.key === 'outputs' && outputsCount > 0"
          class="right-rail__tab-count"
          aria-hidden="true"
        >{{ outputsCount }}</span>
      </button>
    </div>

    <!-- ── Tab panels ────────────────────────────────────────────── -->
    <div
      v-show="activeTab === 'chat'"
      id="right-rail-panel-chat"
      class="right-rail__panel"
      role="tabpanel"
      aria-labelledby="right-rail-tab-chat"
      :hidden="activeTab !== 'chat'"
    >
      <!-- Parent mounts SessionChatView + ChatInputDock here. -->
      <slot name="chat" />
    </div>

    <div
      v-show="activeTab === 'outputs'"
      id="right-rail-panel-outputs"
      class="right-rail__panel"
      role="tabpanel"
      aria-labelledby="right-rail-tab-outputs"
      :hidden="activeTab !== 'outputs'"
    >
      <!-- Parent mounts ProjectWorkspaceRightPanel here. -->
      <slot name="outputs" />
    </div>
  </aside>
</template>

<script setup lang="ts">
import { ref } from 'vue'

// ── Public shape ────────────────────────────────────────────────────────────

export type RightRailTab = 'chat' | 'outputs'

const TABS: ReadonlyArray<{ key: RightRailTab, label: string }> = [
  { key: 'chat', label: '对话' },
  { key: 'outputs', label: 'AI产出' },
]

// ── Props ────────────────────────────────────────────────────────────────────

const props = withDefaults(defineProps<{
  /** Parent-owned active tab. Defaults to 对话. */
  activeTab?: RightRailTab
  /** Optional badge count on the AI产出 tab. */
  outputsCount?: number
}>(), {
  activeTab: 'chat',
  outputsCount: 0,
})

const emit = defineEmits<{
  (e: 'tab-change', tab: RightRailTab): void
}>()

// ── Tab selection + roving-tabindex keyboard nav ──────────────────────────────

const tablistEl = ref<HTMLDivElement | null>(null)

function selectTab(tab: RightRailTab): void {
  if (tab !== props.activeTab) emit('tab-change', tab)
}

function onTablistKeydown(event: KeyboardEvent): void {
  const keys = ['ArrowRight', 'ArrowLeft', 'Home', 'End']
  if (!keys.includes(event.key)) return
  event.preventDefault()

  const currentIndex = TABS.findIndex((t) => t.key === props.activeTab)
  let nextIndex = currentIndex

  if (event.key === 'ArrowRight') nextIndex = (currentIndex + 1) % TABS.length
  else if (event.key === 'ArrowLeft') nextIndex = (currentIndex - 1 + TABS.length) % TABS.length
  else if (event.key === 'Home') nextIndex = 0
  else if (event.key === 'End') nextIndex = TABS.length - 1

  const nextTab = TABS[nextIndex]
  if (!nextTab) return
  selectTab(nextTab.key)

  // Move focus to the newly active tab (roving tabindex).
  const el = tablistEl.value?.querySelector<HTMLButtonElement>(`#right-rail-tab-${nextTab.key}`)
  el?.focus()
}
</script>

